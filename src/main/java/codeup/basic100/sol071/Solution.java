package codeup.basic100.sol071;

import java.util.Scanner;

// 1073
public class Solution {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        while(true) {
            int num = sc.nextInt();
            if(num == 0){
                break;
            }
            System.out.println(num);
        }
    }

}
